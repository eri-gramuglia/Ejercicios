let nroMes: number = Number(prompt("Ingrese el número del mes"));
switch (nroMes) {
  case 1:
    console.log("El mes de Enero");
    break;
  case 2:
    console.log("El mes de Febrero");
    break;
  case 3:
    console.log("El mes de Marzo");
    break;
  case 4:
    console.log("El mes de Abril");
    break;
  case 5:
    console.log("El mes de Mayo");
    break;
  case 6:
    console.log("El mes de Junio");
    break;
  case 7:
    console.log("El mes de Julio");
    break;
  case 8:
    console.log("El mes de Agosto");
    break;
  case 9:
    console.log("El mes de Septiembre");
    break;
  case 10:
    console.log("El mes de Octubre");
    break;
  case 11:
    console.log("El mes de Noviembre");
    break;
  case 12:
    console.log("El mes de Diciembre");
    break;
  default:
    console.log("Ud. no ha ingresado un número de mes válido");
}
